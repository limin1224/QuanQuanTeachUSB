#include <AT91SAM7S64.H>

/********************************************************************
函数功能：IO口初始化。
入口参数：无。
返    回：无。
备    注：无。
********************************************************************/
void LedInit(void)
{
 *AT91C_PIOA_PER=0x07;  //Enable PA0~2
 *AT91C_PIOA_OER=0x07;  //Output Enable PA0~2
 *AT91C_PIOA_SODR=0x07; //OFF all leds
}
/////////////////////////End of function/////////////////////////////

/********************************************************************
函数功能：延时x毫秒函数。
入口参数：毫秒数。
返    回：无。
备    注：无。
********************************************************************/
void DelayXms(unsigned long int x)
{
 unsigned long int i;
 while(x--)
 {
  i=6868;
  while(--i);
 }
}
/////////////////////////End of function/////////////////////////////
